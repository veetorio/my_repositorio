using System;

namespace Trabalho02_calculadora_;
class Calculadora
{
    public double Sum(double _num1, double _num2)
    {
        return _num1 + _num2;
    }
    public double Sub(double _num1, double _num2)
    {
        return _num1 - _num2;
    }
    public double Multi(double _num1, double _num2)
    {
        return _num1 * _num2;
    }
    public double Div(double _num1, double _num2)
    {
        return _num1/_num2;
    }


}
